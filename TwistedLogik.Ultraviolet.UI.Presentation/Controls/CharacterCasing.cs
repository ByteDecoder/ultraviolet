namespace TwistedLogik.Ultraviolet.UI.Presentation.Controls
{
    /// <summary>
    /// Represents the casing which is applies to characters in a <see cref="TextArea"/> control.
    /// </summary>
    public enum CharacterCasing
    {
        /// <summary>
        /// The characters' casing is not changed.
        /// </summary>
        Normal,

        /// <summary>
        /// The characters' casing is forced to lowercase.
        /// </summary>
        Lower,

        /// <summary>
        /// The characters' casing is forced to uppercase.
        /// </summary>
        Upper,
    }
}
